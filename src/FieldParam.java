public class FieldParam
{
    private final int dot = 10, northWall = 0, westWall = 230, southWall = 220, eastWall = 0;

    public int getDot()
    {
        return dot;
    }

    public int getEastWall()
    {
        return eastWall;
    }

    public int getNorthWall()
    {
        return northWall;
    }

    public int getSouthWall()
    {
        return southWall;
    }

    public int getWestWall()
    {
        return westWall;
    }

}
